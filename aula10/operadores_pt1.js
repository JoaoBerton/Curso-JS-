// Operadores Aritméticos

//* Adição = ' + ';
const cash = 12;
let total = 36;
console.log('Resultado da adição é', cash + total);

// Subtração = ' - ';
const valor = 89;
let sub = 56;
console.log('Resultado da subtração é:', valor - sub);

// Multiplicação = ' * ';
const mul = 23;
let multiply = 3;
console.log(`O resultado da multiplicação é : ${mul * multiply}.`);

// Divisão = ' / ';
const repair = 90;
let division = 30;
console.log(`Dividindo as duas variáveis temos: ${repair / division}.`);

// Resto de uma divisão = ' % ';
const empty = 11;
let rest = 2;
console.log(`O resto da divisão entre as duas variáveis é : ${empty % rest}`);

// Potenciação = ' ** ';
const double = 30;
let potency = 5;
console.log(`Quintuplicamos o valor de double e temos: ${double ** potency}.`)